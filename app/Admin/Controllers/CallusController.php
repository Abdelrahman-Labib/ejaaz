<?php

namespace App\Admin\Controllers;

use App\Callus;

use Encore\Admin\Form;
use Encore\Admin\Grid;
use Encore\Admin\Facades\Admin;
use Encore\Admin\Layout\Content;
use App\Http\Controllers\Controller;
use Encore\Admin\Controllers\ModelForm;

class CallusController extends Controller
{
    use ModelForm;

    /**
     * Index interface.
     *
     * @return Content
     */
    public function index()
    {
        return Admin::content(function (Content $content) {

            $content->header('header');
            $content->description('description');

            $content->body($this->grid());
        });
    }

    /**
     * Edit interface.
     *
     * @param $id
     * @return Content
     */
    public function edit($id)
    {
        return Admin::content(function (Content $content) use ($id) {

            $content->header('header');
            $content->description('description');

            $content->body($this->form()->edit($id));
        });
    }

    /**
     * Create interface.
     *
     * @return Content
     */
    public function create()
    {
        return Admin::content(function (Content $content) {

            $content->header('header');
            $content->description('description');

            $content->body($this->form());
        });
    }

    /**
     * Make a grid builder.
     *
     * @return Grid
     */
    protected function grid()
    {
        return Admin::grid(Callus::class, function (Grid $grid) {

            $grid->facebook('Facebook link');
            $grid->twitter('Twitter link');
            $grid->instgram('Instagram link');
            $grid->phone('Phone number');
            $grid->address('Address');

            $grid->created_at();
            $grid->updated_at();
        });
    }

    /**
     * Make a form builder.
     *
     * @return Form
     */
    protected function form()
    {
        return Admin::form(Callus::class, function (Form $form) {

            $form->text("facebook")->rules('nullable');
            $form->text("twitter")->rules('nullable');
            $form->text("instgram")->rules('nullable');
            $form->text("phone")->rules('nullable');
            $form->text("address")->rules('nullable');

        });
    }
}
